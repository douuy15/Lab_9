#include "arithmetic.h"
float sum(float a, float b){
    float sum = a + b;
    return sum;
}
float sub(float a, float b){
    float sub = a - b;
    return sub;
}
float multiply(float a, float b){
    float multiply = a * b;
    return multiply;
}
float divide(float a, float b){
    float divide = a / b;
    return divide;
}